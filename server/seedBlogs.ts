async function seedBlogPosts() {
  const admin = await db.select().from(users).where(eq(users.email, "admin@beagvsglobal.com"));
  if (!admin.length) {
    console.error("Admin user not found. Run seed.ts first.");
    process.exit(1);
  }
  const adminId = admin[0].id;

  for (const post of DEMO_POSTS) {
    const existing = await db.select().from(blogPosts).where(eq(blogPosts.slug, post.slug));
    if (existing.length > 0) {
      console.log(`  Skipping existing post: ${post.title}`);
      continue;
    }
    await db.insert(blogPosts).values({
      authorId: adminId,
      title: post.title,
      slug: post.slug,
      excerpt: post.excerpt,
      contentMarkdown: post.contentMarkdown,
      coverImageUrl: post.coverImageUrl,
      published: post.published,
      ...(post.metaDescription && { metaDescription: post.metaDescription }),
      ...(post.focusKeyword && { focusKeyword: post.focusKeyword }),
      ...(post.tags && { tags: post.tags }),
      ...(post.ogTitle && { ogTitle: post.ogTitle }),
      ...(post.ogDescription && { ogDescription: post.ogDescription }),
    } as any);
    console.log(`  ✓ Seeded: ${post.title}`);
  }

  console.log("Blog posts seeded!");
  process.exit(0);
}

seedBlogPosts().catch(e => { console.error(e); process.exit(1); });
